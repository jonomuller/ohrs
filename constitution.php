          <h3 id="name">NAME AND OBJECTS</h3>
          <ol>
            <li>The Society is called the Oxford Human Rights Society. The
            Society's objects increasing the understanding of human rights and
            issues of equality for oppressed and marginalised groups in the
            University of Oxford; and the income and property of the Club shall
            be applied solely to those objects.</li>
            <li>Membership
              <ol type="a">
                <li>The members of the Society shall be those who are eligible
                and apply for membership of the Society, who are admitted to and
                maintained in membership by the Committee, and who have paid the
                relevant Society subscription.</li>
                <li>All student members of the University, and all persons whose
                names are on the University&rsquo;s Register of Visiting
                Students, shall be eligible to become members of the Society. A
                member shall continue to be eligible until he or she is given
                permission to supplicate for his or her degree, diploma or
                certificate, regardless of whether or not he or she continues
                to be liable to pay fees to the University.</li>
                <li>The Committee may also, at its discretion, admit to
                  membership:-
                  <ol type="i">
                    <li>students registered to read for diplomas and
                    certificates in the University;</li>
                    <li>student members of Permanent Private Halls who are not
                    student members of the University;</li>
                    <li>members of Ruskin College; Ripon College, Cuddesdon and
                    the Oxford Institute of Legal Practice;</li>
                    <li>members of the Westminster Institute of Oxford Brookes
                    University who are registered to read for degrees or other
                    qualifications validated by the University of Oxford.</li>
                  </ol>
                </li>
              </ol>
            </li>
            <li>The Committee may remove a person from membership for good cause.
            The person concerned may appeal against such removal to the Senior
            Member.</li>
          </ol>
          <h3 id="meetings">MEETINGS OF THE MEMBERS</h3>
          <ol start="4">
            <li>There shall be an Annual General Meeting for all the members of
            the Society in Trinity Term, convened by the Secretary on not less
            than fourteen days' notice.</li>
            <li>The Annual General Meeting will:
              <ol type="a">
                <li>receive the annual report of the Committee for the previous
                year and the annual accounts of the Society for the previous
                year, the report and accounts having been approved by the
                Committee;</li>
                <li>receive a report from the Committee on the Society's
                compliance with paragraph 2 above;</li>
                <li>elect Members of the Committee in accordance with paragraph
                19 below: the Committees nominations for the Officers and
                the Senior Member shall be contained in the notice of the
                Meeting: any alternative nominations must be seconded and have
                the consent of the nominee, and must be received in writing by
                the Secretary not less than seven days before the date of the
                Meeting: nominations for the other Committee Members may be
                taken from the floor of the meeting;</li>
                <li>consider any motions of which due notice has been given, and
                any other relevant business.</li>
              </ol>
            </li>
            <li>An Extraordinary General Meeting may be called in any Full Term;
            by the President, the Secretary or the Treasurer on not less than
            seven days' notice; or on a written requisition by [seven] or more
            members, stating the reason for which the meeting is to be called,
            and delivered to the Secretary not less than fourteen days before
            the date of the Meeting.</li>
            <li>Prior to all General Meetings notice of the agenda shall be sent
            out with the notice of the Meeting.</li>
            <li>The quorum for a General Meeting shall be <________> members
            present in person or by proxy, of whom [three] must be members of
            the Committee. When any financial business is to be transacted there
            must be present the Treasurer, or a member of the Committee deputed
            by the Treasurer to represent his or her views to the Meeting
            (provided that where it is a case of a deputy, the only financial
            business transacted shall be that which was set out in the agenda
            accompanying the notice of the meeting).</li>
          </ol>
          <h3 id="committee">THE COMMITTEE</h3>
          <ol start="9">
            <li>The affairs of the Club shall be administered by a Committee
            consisting of not more than <________> persons, which shall
            determine the subscriptions payable by the members of the Society,
            and have ultimate responsibility for the activities of the Society.
            Members of the University shall at all times make up the majority of
            the members of the Committee. The Committee shall have control of
            the funds and property of the Club, and of its administration.</li>
            <li>The quorum for a Committee meeting shall be <________> members
            present in person. When any financial business is to be transacted,
            there must be present either the Treasurer or a member of the
            Committee deputed by the Treasurer to represent his or her views to
            the meeting.</li>
            <li>The Committee shall be made up of the President, the Secretary,
            the Treasurer (together, the "Office Holders"; and their offices are
            referred to as "the Offices"), the Senior Member and <________>
            other persons.</li>
            <li>The President shall have the right to preside at all meetings of
            the members of the Society and at all meetings of the Committee.
            Should the President be absent, or decline to take the chair, the
            Committee shall elect another member of the Committee to chair the
            meeting.</li>
            <li>The Secretary shall:
              <ol type="a">
                <li>maintain a register of the members of the Society,</li>
                <li>give notice of meetings of the members and the Committee;</li>
                <li>draw up the agendae for and the minutes of those meetings;</li>
                <li>notify the Proctors not later than the end of the second week
                of every Full Term of the programme of meetings which has been
                arranged for that term (e.g. by providing them a copy of the term
                card);</li>
                <li>provide the Insurance Section with full details of any
                insurance cover purchased from or through a national governing
                body pursuant to paragraph 2(c) above; and</li>
              </ol>
            </li>
            <li>The Treasurer shall:
              <ol type="a">
                <li>keep proper records of the Society's financial transactions
                in accordance with current accepted accounting rules and practices;</li>
                <li>ensure that bills are paid and cash is banked in accordance
                with the procedures developed under (b);</li>
                <li>prepare an annual budget for the Society, and regularly
                inform the Committee of progress against that budget;</li>
                <li>ensure that all statutory returns are made including VAT,
                income tax and corporation tax if appropriate;</li>
                <li>develop and maintain a manual of written procedures for all
                aspects of the Treasurer's responsibilities;</li>
              </ol>
            </li>
            <li>The Senior Member shall:-
              <ol type="a">
                <li>hear appeals from removal from membership under paragraph
                6 above;</li>
                <li>ensure that adequate advice and assistance is available to
                the Secretary and the Treasurer in the performance of their
                responsibilities under paragraphs 16 and 17 above; and</li>
              </ol>
            </li>
            <li>The members of the Committee shall be elected by the members of
            the Society annually, and shall be eligible for re‑election. The
            members of the Society shall not appoint several individuals jointly
            to hold any of the Offices, nor allow any individual to hold more
            than one Office at a time.</li>
            <li>If during the period between the annual elections to offices any
            vacancies occur amongst the members of Committee, the Committee shall
            have the power of filling the vacancy or vacancies up to the next
            Annual General Meeting by co‑optation.</li>
            <li>Each Office Holder must, on relinquishing his or her appointment,
            promptly hand to his or her successor in Office (or to another
            member of the Society nominated by the Committee) all official
            documents and records belonging to the Society, together with (on
            request from the Committee) any other property of the Society which
            may be in his or her possession; and must complete any requirements
            to transfer authority relating to control of the Society's bank
            accounts, building society accounts, or other financial affairs.</li>
            <li>Without derogating from its primary responsibility, the
            Committee may delegate its functions to finance and general purposes
            and other subcommittees which are made up exclusively of members of
            the Committee.</li>
            <li>The Committee shall have power to make regulations and by‑laws
            in order to implement the paragraphs of this Constitution, and to
            settle any disputed points not otherwise provided for in this
            Constitution. Any alteration to this Constitution shall require the
            approving vote of two-thirds of those present in person or by proxy
            at a General Meeting.</li>
            <li>No member of the Committee shall be removed from office except by
            the approving votes of two‑thirds of those present in person or by
            proxy at a General Meeting.</li>
          </ol>
          <h3 id="indemnity">INDEMNITY</h3>
          <ol start="22">
            <li>So far as may be permitted by law, every member of the Committee
            and every officer of the Club shall be entitled to be indemnified by
            the Society against all costs, charges, losses, expenses and
            liabilities incurred by him or her in the execution or discharge of
            his or her duties or the exercise of his or her powers, or otherwise
            properly in relation to or in connection with his or her duties. This
            indemnity extends to any liability incurred by him or her in
            defending any proceedings, civil or criminal, which relate to
            anything done or omitted or alleged to have been done or omitted by
            him or her as a member of the Committee or officer of the Society and
            in which judgement is given in his or her favour (or the proceedings
            are otherwise disposed of without any finding or admission of any
            material breach of duty on his or her part), or in which he or she is
            acquitted, or in connection with any application under any statute
            for relief from liability in respect of any such act or omission in
            which relief is granted to him or her by the Court.</li>
            <li>So far as may be permitted by law, the Society may purchase and
            maintain for any member of the Committee or officer of the Society
            insurance cover against any liability which by virtue of any rule of
            law may attach to him or her in respect of any negligence, default,
            breach of duty or breach of trust of which he or she may be guilty in
            relation to the Society and against all costs, charges, losses and
            expenses and liabilities incurred by him or her and for which he or
            she is entitled to be indemnified by the Society by virtue of
            paragraph 22.</li>
          </ol>
          <h3 id="dissolution">DISSOLUTION</h3>
          <ol start="24">
            <li>The Society may be dissolved at any time by the approving votes
            of two‑thirds of those present in person or by proxy at a General
            Meeting.</li>
          </ol>
          <h3 id="interpretation">INTERPRETATION</h3>
          <ol start="25">
          <li>Any question about the interpretation of this Constitution shall be
          settled by the Senior Member.</li>
          </ol>
